class switchexample {
  void example(String fruit) {
    switch(fruit) {
      case "apple" || "APPLE":
      print("$fruit is Added");
      case "Guava" || "GUAVA":
      print("$fruit is Added");
      case "mango" || "MANGO":
      print("$fruit is Added");
      case "Banana" || "BANANA":
      print("$fruit is Added");
    
      default:
      print("$fruit is not available");
    }
  }
}
